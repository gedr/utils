package ru.gedr.tuples;

import ru.gedr.comparators.GenericComparator;

public abstract class Octet<Ta, Tb, Tc, Td, Te, Tf, Tg, Th> extends Septet<Ta, Tb, Tc, Td, Te, Tf, Tg> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================
	private static final long serialVersionUID = 1L;

	// =================================================================================================================
	// Fields
	// =================================================================================================================

	// =================================================================================================================
	// Constructors
	// =================================================================================================================

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================
	@Override
	public int getDimension() {
		return 8;
	}

	@SuppressWarnings("unchecked")
	@Override
	public <TT> TT getBy(int index) {
		if (index == 8) {
			return (TT) getEighth();
		}
		return super.getBy(index);
	}

	@Override
	public String toString(String format) {
		return String.format(format, getFirst(), getSecond(), getThird(), getFourth(), getFifth(), getSixth(),
				getSeventh(), getEighth());
	}

	@SuppressWarnings("unchecked")
	public int compareTo(Object o) {
		int res = super.compareTo(o);
		if (res == 0) {
			try {
				Octet<?, ?, ?, ?, ?, ?, ?, ?> t = (Octet<?, ?, ?, ?, ?, ?, ?, ?>) o;
				res = GenericComparator.compare(this.getEighth(), (Th) t.getEighth());
			} catch (Exception e) {
				throw new UnsupportedOperationException(e);
			}
		}
		return res;
	}

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================

	// =================================================================================================================
	// Methods
	// =================================================================================================================
	public abstract Th getEighth();

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}