package ru.gedr.tuples;

public class ImmutableQuintet<Ta, Tb, Tc, Td, Te> extends Quintet<Ta, Tb, Tc, Td, Te> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================
	private static final long serialVersionUID = 1L;

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	protected Ta first;
	protected Tb second;
	protected Tc third;
	protected Td fourth;
	protected Te fifth;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public ImmutableQuintet(Ta first, Tb second, Tc third, Td fourth, Te fifth) {
		this.first = first;
		this.second = second;
		this.third = third;
		this.fourth = fourth;
		this.fifth = fifth;
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================
	@Override
	public Ta getFirst() {
		return first;
	}

	@Override
	public Tb getSecond() {
		return second;
	}

	@Override
	public Tc getThird() {
		return third;
	}

	@Override
	public Td getFourth() {
		return fourth;
	}

	@Override
	public Te getFifth() {
		return fifth;
	}

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================

	// =================================================================================================================
	// Methods
	// =================================================================================================================
	public static <A, B, C, D, E> ImmutableQuintet<A, B, C, D, E> of(A first, B second, C third, D fourth, E fifth) {
		return new ImmutableQuintet<A, B, C, D, E>(first, second, third, fourth, fifth);
	}

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}