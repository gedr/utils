package ru.gedr.tuples;

public class ImmutableUnit<T> extends Unit<T>{
	// =================================================================================================================
	// Constants
	// =================================================================================================================
	private static final long serialVersionUID = 1L;

	// =================================================================================================================
	// Fields
	// =================================================================================================================
	protected T value;

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public ImmutableUnit(T value) {
		this.value = value;
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================
	@Override
	public T getValue() {
		return value;
	}

	@Override
	public T getFirst() {
		return value;
	}

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================

	// =================================================================================================================
	// Methods
	// =================================================================================================================
	public static <V> ImmutableUnit<V> of(V value) {
		return new ImmutableUnit<V>(value);
	}

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}
