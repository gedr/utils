package ru.gedr.tuples;

import ru.gedr.comparators.GenericComparator;

public abstract class Quintet<Ta, Tb, Tc, Td, Te> extends Quartet<Ta, Tb, Tc, Td> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================
	private static final long serialVersionUID = 1L;

	// =================================================================================================================
	// Fields
	// =================================================================================================================

	// =================================================================================================================
	// Constructors
	// =================================================================================================================

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================
	@Override
	public int getDimension() {
		return 5;
	}

	@SuppressWarnings("unchecked")
	@Override
	public <TT> TT getBy(int index) {
		if (index == 5) {
			return (TT) getFifth();
		}
		return super.getBy(index);
	}

	@Override
	public String toString(String format) {
		return String.format(format, getFirst(), getSecond(), getThird(), getFourth(), getFifth());
	}

	@SuppressWarnings("unchecked")
	public int compareTo(Object o) {
		int res = super.compareTo(o);
		if (res == 0) {
			try {
				Quintet<?, ?, ?, ?, ?> t = (Quintet<?, ?, ?, ?, ?>) o;
				res = GenericComparator.compare(this.getFifth(), (Te) t.getFifth());
			} catch (Exception e) {
				throw new UnsupportedOperationException(e);
			}
		}
		return res;
	}

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================

	// =================================================================================================================
	// Methods
	// =================================================================================================================
	public abstract Te getFifth();

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}
