package ru.gedr.tuples;

public class MutableTriple<Ta, Tb, Tc> extends ImmutableTriple<Ta, Tb, Tc> {
	// =================================================================================================================
	// Constants
	// =================================================================================================================
	private static final long serialVersionUID = 1L;

	// =================================================================================================================
	// Fields
	// =================================================================================================================

	// =================================================================================================================
	// Constructors
	// =================================================================================================================
	public MutableTriple() {
		super(null, null, null);
	}

	public MutableTriple(Ta first, Tb second, Tc third) {
		super(first, second, third);
	}

	// =================================================================================================================
	// Methods for/from SuperClass/Interface
	// =================================================================================================================

	// =================================================================================================================
	// Getter & Setter
	// =================================================================================================================

	// =================================================================================================================
	// Methods
	// =================================================================================================================
	public static <A, B, C> MutableTriple<A, B, C> of(A first, B second, C third) {
		return new MutableTriple<A, B, C>(first, second, third);
	}

	public void setFirst(Ta first) {
		this.first = first;
	}

	public void setSecond(Tb second) {
		this.second = second;
	}

	public void setThird(Tc third) {
		this.third = third;
	}

	public void setLeft(Ta value) {
		this.first = value;
	}

	public void setMiddle(Tb value) {
		this.second = value;
	}

	public void setRight(Tc value) {
		this.third = value;
	}

	// =================================================================================================================
	// Inner and Anonymous Classes
	// =================================================================================================================
}
